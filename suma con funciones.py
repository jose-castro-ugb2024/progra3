def suma_numero(num1, num2):
    #suma dos numeros y devuelve el resultado
    return num1 + num2

#pedir la variable
num1=int(input("Introduce el numero primero para sumar:  "))
num2=int(input("Introduce el numero segundo para sumar:  "))

#imprimir el resultado
print(f"El suma de {num1}  y {num2} es {suma_numero(num1, num2)}")

